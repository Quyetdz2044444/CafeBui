package quyettvph35419.fpoly.cafebuipho.Model;

public class Size {
    private int maSize;
    private String size;

    public Size() {
    }

    public Size(int maSize, String size) {
        this.maSize = maSize;
        this.size = size;
    }

    public int getMaSize() {
        return maSize;
    }

    public void setMaSize(int maSize) {
        this.maSize = maSize;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }


}
